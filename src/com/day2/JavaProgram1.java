package com.day2;

public class JavaProgram1 {
    public static void main(String args[]){
        //String
        String name = "Peter";
        System.out.println("The name is " + name);
        //numbers (int)
        int number = 1;
        System.out.println("The number is " + number);
        //decimal (double)
        double floatedNumber = 2.124;
        System.out.println("The float number is " + floatedNumber);
        //boolean
        boolean isPerson = true;
        System.out.println("The boolean is " + isPerson);
        //char
        char letter = 'A';
        System.out.println("The letter is " + letter);
    }
}
